package com.github.projetoleaf.data;

public class Quantidade_refeicao {
	
	private int id;
	private int subsidiada;
	private int	custo;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getSubsidiada() {
		return subsidiada;
	}
	public void setSubsidiada(int subsidiada) {
		this.subsidiada = subsidiada;
	}
	public int getCusto() {
		return custo;
	}
	public void setCusto(int custo) {
		this.custo = custo;
	}
}
